#include <unistd.h>
#include <stdio.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		if ((nb / 10) > 0)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		else 
		{
			c = nb + '0';
			write(1, &c, 1);
		}
	}
}

